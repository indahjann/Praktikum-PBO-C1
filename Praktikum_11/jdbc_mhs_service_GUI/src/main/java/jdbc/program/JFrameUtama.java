    private void clearFields() {
        textFieldNamaAdd.setText("");
        textFieldIdEdit.setText("");
        textFieldNamaEdit.setText("");
        textFieldIdHapus.setText("");
        textFieldNamaHapus.setText("");
    }

    private void refreshData() {
        try {
        // Ambil data dari database
            List<Mahasiswa> list = mhsService.getAll();
            // Clear model
            modelID.clear();
            modelNama.clear();
            // Tambahkan data ke model
            for (Mahasiswa mhs : list) {
                modelID.addElement(String.valueOf(mhs.getId()));
                modelNama.addElement(mhs.getNama());
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this,
                "Error mengambil data: " + ex.getMessage(),
                "Error",
                JOptionPane.ERROR_MESSAGE);
        }
    }

    private boolean validateInput(String nama) {
        if (nama == null || nama.trim().isEmpty()) {
            return false;
        }
        return true;
    }

    private boolean validateInputEdit(String id, String nama) {
        // Check if id and nama are not empty
        if (id == null || id.trim().isEmpty() || nama == null || nama.trim().isEmpty()) {
            return false;
        }
        
        // Check if id is numeric
        if (!id.matches("\\d+")) {
            return false;
        }
        
        return true;
    }

