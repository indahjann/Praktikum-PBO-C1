    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed
        // TODO add your handling code here:
        try {
            String nama = textFieldNamaAdd.getText();
            if(validateInput(nama)) {
                Mahasiswa mhs = new Mahasiswa(0, nama);
                mhsService.add(mhs);
                clearFields();
                refreshData();
            }
        } catch (SQLException ex) {
            System.err.println("Error adding data: " + ex.getMessage());
        }
    }//GEN-LAST:event_jButtonAddActionPerformed

    private void jButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditActionPerformed
        // TODO add your handling code here:
        try {
            String idStr = textFieldIdEdit.getText();
            String nama = textFieldNamaEdit.getText();
            
            if(validateInputEdit(idStr, nama)) {
                int id = Integer.parseInt(idStr);
                Mahasiswa mhs = new Mahasiswa(id, nama);
                mhsService.update(mhs);
                clearFields();
                refreshData();
            }
        } catch (SQLException ex) {
            System.err.println("Error updating data: " + ex.getMessage());
        }
    }//GEN-LAST:event_jButtonEditActionPerformed

    private void jButtonHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonHapusActionPerformed
        // TODO add your handling code here:
        try {
            String idStr = textFieldIdHapus.getText();
            if(!idStr.trim().isEmpty() && idStr.matches("\\d+")) {
                int id = Integer.parseInt(idStr);
                mhsService.delete(id);
                clearFields();
                refreshData();
            }
        } catch (SQLException ex) {
            System.err.println("Error deleting data: " + ex.getMessage());
        }
    }//GEN-LAST:event_jButtonHapusActionPerformed

    private void jButtonRisetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRisetActionPerformed
        // TODO add your handling code here:
        try {
            mhsService.indexReset();
            refreshData();
        } catch (SQLException ex) {
            System.err.println("Error resetting index: " + ex.getMessage());
        }
    }//GEN-LAST:event_jButtonRisetActionPerformed

